package com.anudip.day2;
/*Do - while --> 
 * 
 * First it will execute some set of instructions
 * and then it will check the condition.
 */
public class ExampleForDoWhile {

	public static void main(String[] args) {
		
		int x = 1;
		
		do {
			System.out.println(x);
			x++;
		}
		while(x<=5);

	}

}
